import { mkdirSync, writeFileSync } from 'node:fs';
import { dirname } from 'node:path';

import { reportError } from '@williamthorsen/nmr-core';
import { stringify } from 'yaml';

import { CONFIG_FILE_PATH, loadConfig } from '../loadConfig.ts';
import type { RepoLabelsConfig } from '../types.ts';
import { validateConfig } from '../validateConfig.ts';
import { hashPresetFile } from './presets.ts';
import { resolveLabels } from './resolveLabels.ts';
import { checkRetiredSyncLabelsConfig } from './retiredConfig.ts';
import type { LabelDefinition } from './types.ts';

/** Output path for the generated labels file. */
export const LABELS_OUTPUT_PATH = '.github/labels.yaml';

/** Format resolved labels as YAML with generated header including preset hashes. */
export function formatLabelsYaml(labels: LabelDefinition[], presetHashes: Map<string, string>): string {
  const headerLines = ['# Generated by release-kit sync-labels — do not edit.', `# Source: ${CONFIG_FILE_PATH}`];
  // eslint-disable-next-line unicorn/no-array-sort -- spread already creates a fresh copy
  for (const [name, hash] of [...presetHashes].sort(([a], [b]) => a.localeCompare(b))) {
    headerLines.push(`# ${name} preset hash: ${hash}`);
  }

  // Use single quotes when quoting is needed; let yaml decide when to quote; disable line wrapping
  const yamlBody = stringify(labels, { singleQuote: true, lineWidth: 0 });
  return headerLines.join('\n') + '\n' + yamlBody;
}

/**
 * Load `.config/release-kit.config.ts` and return its `repoLabels` block.
 *
 * Reports an actionable error and returns `undefined` when the config file is missing,
 * fails to load or validate, or carries no `repoLabels` block.
 */
export async function loadRepoLabelsConfig(): Promise<RepoLabelsConfig | undefined> {
  let raw: unknown;
  try {
    raw = await loadConfig();
  } catch (error: unknown) {
    const message = error instanceof Error ? error.message : String(error);
    reportError(`Failed to load config: ${message}`);
    return undefined;
  }

  if (raw === undefined) {
    reportError(`No config file found at ${CONFIG_FILE_PATH}. Run \`release-kit sync-labels init\` first.`);
    return undefined;
  }

  const { config, errors } = validateConfig(raw);
  if (errors.length > 0) {
    process.stderr.write('Invalid config:\n');
    for (const err of errors) {
      process.stderr.write(`  ❌ ${err}\n`);
    }
    return undefined;
  }

  if (config.repoLabels === undefined) {
    reportError(
      `No \`repoLabels\` block found in ${CONFIG_FILE_PATH}. Run \`release-kit sync-labels init\` to seed one.`,
    );
    return undefined;
  }

  return config.repoLabels;
}

/**
 * Run the `sync-labels generate` command.
 *
 * Loads the `repoLabels` block from `.config/release-kit.config.ts`, resolves presets and
 * label adjustments, and writes `.github/labels.yaml`. Returns 0 on success, 1 on failure.
 */
export async function generateCommand(): Promise<number> {
  if (checkRetiredSyncLabelsConfig()) {
    return 1;
  }

  const repoLabels = await loadRepoLabelsConfig();
  if (repoLabels === undefined) {
    return 1;
  }

  let labels;
  try {
    labels = resolveLabels(repoLabels);
  } catch (error: unknown) {
    const message = error instanceof Error ? error.message : String(error);
    reportError(`Failed to resolve labels: ${message}`);
    return 1;
  }

  const presetHashes = new Map<string, string>();
  for (const presetName of repoLabels.extends ?? []) {
    presetHashes.set(presetName, hashPresetFile(presetName));
  }

  const content = formatLabelsYaml(labels, presetHashes);

  try {
    mkdirSync(dirname(LABELS_OUTPUT_PATH), { recursive: true });
    writeFileSync(LABELS_OUTPUT_PATH, content, 'utf8');
  } catch (error: unknown) {
    const message = error instanceof Error ? error.message : String(error);
    reportError(`Failed to write ${LABELS_OUTPUT_PATH}: ${message}`);
    return 1;
  }

  console.info(`Generated ${LABELS_OUTPUT_PATH} with ${String(labels.length)} labels.`);
  return 0;
}
